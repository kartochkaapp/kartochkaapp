const hashText = (value) => {
  const source = String(value || "");
  if (!source) return "";
  return crypto.createHash("sha256").update(source).digest("hex").slice(0, 16);
};

const resolveInstruction = (instructionPath) => {
  const normalizedPath = toText(instructionPath) || DEFAULT_IMPROVE_INSTRUCTION_PATH;
  const text = getBundledPromptText(normalizedPath);
  return {
    path: normalizedPath,
    source: text ? "bundled" : "missing",
    text,
    hash: hashText(text),
  };
};

const buildUserWishesBlock = (userPrompt) => {
  const safeUserPrompt = toText(userPrompt);
  if (!safeUserPrompt) return "";

  return [
    "User wishes:",
    safeUserPrompt,
    "Follow these wishes if they do not break product truthfulness, readability, marketplace quality, or conversion strength.",
  ].join("\n");
};

const buildExactTextPreservationBlock = (preserveSourceTextExactly) => {
  if (!preserveSourceTextExactly) return "";

  return [
    "SOURCE TEXT MODE: PRESERVE EXACT WORDING",
    "All important visible product-card text from the uploaded source image must be preserved one-to-one.",
    "Keep the exact original wording in Russian wherever that text is still present in the result.",
    "Do not paraphrase, rewrite, shorten, expand, translate, reinterpret, or replace the original claims.",
    "You may change typography, line breaks, scale, grouping, emphasis, and placement for better design.",
    "But the wording itself must stay identical to the source card whenever retained.",
  ].join("\n");
};

const buildDirectImprovePrompt = (params = {}) => {
  const instruction = resolveInstruction(params.instructionPath);
  const userWishesBlock = buildUserWishesBlock(params.userPrompt);
  const exactTextPreservationBlock = buildExactTextPreservationBlock(params.preserveSourceTextExactly);

  const prompt = [
    ...DIRECT_IMPROVE_PROMPT,
    "",
    exactTextPreservationBlock,
    "",
    userWishesBlock,
  ].filter(Boolean).join("\n");

  return {
    prompt,
    debug: {
      promptVersion: PROMPT_VERSION,
      instructionPath: instruction.path,
      instructionSource: instruction.source,
      instructionHash: instruction.hash,
      userPromptHash: hashText(params.userPrompt),
      preserveSourceTextExactly: Boolean(params.preserveSourceTextExactly),
    },
  };
};

const createEnhanceCardPromptService = () => {
  return {
    buildPrompt(params) {
      return buildDirectImprovePrompt(params);
    },
  };
};

